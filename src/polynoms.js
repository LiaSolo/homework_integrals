export function deg0() {
    return 120
}

export function deg0String() {
    return '120'
}

export function deg1(x){
    return 120*x + 24
}

export function deg1String() {
    return '120x+24'
}

export function deg2(x){
    return 60*x**2 + 24*x + 6
}

export function deg2String() {
    return '60x^2+24x+6'
}

export function deg3(x){
    return 20*x**3 + 12*x**2 + 6*x + 2
}

export function deg3String() {
    return '20x^3+12x^2+6x+2'
}

export function deg4(x){
    return 5*x**4 + 4*x**3 + 3*x**2 + 2*x + 1
}

export function deg4String(){
    return '5x^4+4x^3+3x^2+2x+1'
}

export function deg5(x){
    return x**5 + x**4 + x**3 + x**2 + x
}

export function deg5String(){
    return 'x^5+x^4+x^3+x^2+x'
}